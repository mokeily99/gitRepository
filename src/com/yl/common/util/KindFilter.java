	private static void moveFile(String srcPath,String descPath){
		File srcFile = new File(srcPath); 
		if(srcFile.isDirectory()){
			//创建拷贝目标目录
			createDirectory(descPath); 
			//递归查找文件
			moveFile(srcFile.getAbsolutePath(),descPath); 
			System.out.println(srcPath);
		} else {
			copyFile(srcFile.getAbsolutePath(),descPath); 
			System.out.println(srcPath);
		}
	}
	
	private static void createDirectory(String targetPath) {
		File dir = new File(targetPath);
		if (!dir.exists()) {
			dir.mkdirs();
		}
	}
	
	private static void copyFile(String srcPath,String destPath){
		File srcFile = new File(srcPath); 
		try{
			//获取文件的输入流
			BufferedInputStream bis = new BufferedInputStream(new FileInputStream(srcFile)); 
			//获取文件输出流
			BufferedOutputStream bos = new BufferedOutputStream(new FileOutputStream(new File(destPath))); 
			//定义缓存区
			byte [] buffer = new byte [1024]; 
			//定义存储每次读取的字节数
			int len = 0; 
			while((len = bis.read(buffer))!= - 1){
				bos.write(buffer,0,len); 
				//删除源文件
				srcFile.delete(); 
			}
		} catch(Exception e){ 
			e.printStackTrace(); 
		}
	}
