public class Calledtrunk {

	private String from;
	
	private String to;
	
	private String trunk;
	
	private String callerdest;
	
	private String uuid;

	public String getCallerdest() {
		return callerdest;
	}

	public void setCallerdest(String callerdest) {
		this.callerdest = callerdest;
	}

	public String getFrom() {
		return from;
	}

	public void setFrom(String from) {
		this.from = from;
	}

	public String getTo() {
		return to;
	}

	public void setTo(String to) {
		this.to = to;
	}

	public String getTrunk() {
		return trunk;
	}

	public void setTrunk(String trunk) {
		this.trunk = trunk;
	}

	public String getUuid() {
		return uuid;
	}

	public void setUuid(String uuid) {
		this.uuid = uuid;
	}
}
